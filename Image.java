package com.example.stickhero;
public class Image {
    private String path;
    private int width;
    private int height;

    public String getPath() {
        return path;
    }

    public int getWidth() {
        return width;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getHeight() {
        return height;
    }
}
